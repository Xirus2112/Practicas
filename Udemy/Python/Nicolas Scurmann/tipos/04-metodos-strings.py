"""Imprimiendo Metodos"""
animal = "   chanCHito feliz    "
# UPPER convierte la cadena de String en mayuscula
print(animal.upper())
# UPPER convierte la cadena de String en minisculas
print(animal.lower())
# CAPITALIZE convierte el primer String en mayuscula
print(animal.strip().capitalize())
# TITLE convierte las primeros String en mayuscula
print(animal.title())
# STRIP elimina los espacios que hay a la derecha o izquierda del strings
print(animal.strip())
# STRIP elimina los espacios que hay a la izquierda del strings
print(animal.lstrip())
# STRIP elimina los espacios que hay a la derecha del strings
print(animal.rstrip())