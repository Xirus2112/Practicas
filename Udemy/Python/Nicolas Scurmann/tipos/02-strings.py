# Variables tipo Strings
nombre_curso = "Ultimate Python"

# para agregar cadenas de textos mas amplias se coloca triple comillas dobles
descripcion_curso = """
Ultimate python este curso, este curso contempla los 
detalles que necesitas aprender para encontrar un 
trabajo como programado
"""
print(len(nombre_curso))
# Ingresando al primer indice de la palabra
print(nombre_curso[0])
# Cortando los datos almacenados en la variable
print(nombre_curso[0:8])
# Tomando el indice 9 
print(nombre_curso[9:])
# Tomando el indice 8 de derecha a izquierda
print(nombre_curso[:8])
# Tomando todos los datos siin agregarle valor
print(nombre_curso[:])

